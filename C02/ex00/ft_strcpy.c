char	*ft_strcpy(char *dest, char *src)
{
	char	*tmpd;

	tmpd = dest;
	while (*src)
	{
		*tmpd = *src;
		src++;
		tmpd++;
	}
	*tmpd = *src;
	return (dest);
}
