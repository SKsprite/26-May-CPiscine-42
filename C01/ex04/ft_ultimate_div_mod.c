void	ft_ultimate_div_mod(int *a, int *b)
{
	int	tmpd;
	int	tmpr;

	tmpr = *a;
	tmpd = *a / *b;
	while (tmpr - *b >= 0)
	{
		tmpr = tmpr - *b;
	}
	*b = tmpr;
	*a = tmpd;
}
