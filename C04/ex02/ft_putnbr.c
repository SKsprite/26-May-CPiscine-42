#include <unistd.h>

void	wt(char *str)
{
	while (*str)
		write(1, str++, 1);
}

void	nbr(int nb)
{
	if (nb == 0)
		return ;
	nbr(nb / 10);
	wt((char []){'0' + nb % 10, '\0'});
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		wt("-2147483648");
		return ;
	}
	if (nb == 0)
	{
		wt("0");
		return ;
	}
	if (nb < 0)
	{
		wt("-");
		nb *= -1;
	}
	nbr(nb);
}
