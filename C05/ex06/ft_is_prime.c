int	ft_is_prime(int nb)
{
	int	half;
	int	i;

	if (nb <= 1)
		return (0);
	half = nb / 2;
	i = 2;
	while (i <= half)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
