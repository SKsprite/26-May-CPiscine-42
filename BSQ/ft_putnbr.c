#include "bsq.h"

void	ft_putnbr(int num)
{
	char	c;

	c = '0';
	if (num < 10)
	{
		c += num;
		ft_putchar(c);
		return ;
	}
	ft_putnbr(num / 10);
	c += num % 10;
	ft_putchar(c);
	return ;
}
