#include <unistd.h>

void	wt(char *c)
{
	while (*c)
		write(1, c++, 1);
}

int	main(int argc, char *argv[])
{
	if (argc < 0)
		return (1);
	wt(argv[0]);
	wt("\n");
	return (0);
}
